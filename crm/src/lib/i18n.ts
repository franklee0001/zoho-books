export function t(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const val = dict[locale]?.[key] ?? dict.en[key] ?? key;
  if (!vars) return val;
  return Object.entries(vars).reduce(
    (str, [k, v]) => str.replaceAll(`{${k}}`, String(v)),
    val,
  );
}
